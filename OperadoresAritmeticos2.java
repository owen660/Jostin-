class OperadoresAritmeticos2 {
    public static void main(String[] args) {
        double numero1 = 7.0;
        double numero2 = 5.0;
        System.out.println("Operaciones con\"numero1\" y \"numero2:\"");
        System.out.println( numero1 + numero2);
        System.out.println( numero1 - numero2);
        System.out.println( numero1 * numero2);
        System.out.println( numero1 / numero2);
        System.out.println( numero1 % numero2);
    } // Fin del método main
} // Fin de la clase OperadoresAritméticos